                <p><em><span style="font-weight: 400;">Hạt kê là một loại ngũ cốc nhưng có chứa hàm
                            lượng dinh dưỡng cao. Và loại hạt này thường được dùng để chế biến thành
                            nhiều món ăn ngon, đặc biệt là các món chè. Trong bài viết dưới đây,
                            mình sẽ hướng dẫn cho bạn 3 <strong>cách nấu chè hạt kê</strong> thơm
                            ngon và cực đơn giản nhé!</span></em></p>
             
                <h2><span id="1_Cach_nau_che_hat_ke_dau_xanh_thom_ngon_de_lam"><b>1. Cách nấu chè
                            hạt kê đậu xanh thơm ngon, dễ làm</b></span></h2>
                <h3><span id="Nguyen_lieu"><b>Nguyên liệu</b></span></h3>
                <ul>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">200g hạt kê</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">300g đậu xanh bỏ
                            vỏ</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">30g
                            bột sắn dây</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">200g đường phèn</span>
                    </li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Dừa
                            nạo</span></li>
                </ul>
                <h3><span id="Cach_nau_che_hat_ke_dau_xanh_bo_vo"><b>Cách nấu chè hạt kê đậu xanh bỏ
                            vỏ</b></span></h3>
                <h4><span id="Buoc_1_So_che_nguyen_lieu"><b>Bước 1: Sơ chế nguyên liệu</b></span>
                </h4>
                <ul>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bạn
                            nên chọn những hạt kê có màu vàng đậm, bỏ hạt lép, loại hạt này sẽ khiến
                            món chè thêm đậm đà và ngon hơn, Sau khi mua hạt kê về bạn rửa sạch rồi
                            ngâm với nước khoảng 5-6 tiếng cho hạt kê nở mềm.</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Đậu
                            xanh bạn nên đem đãi sạch cặn bẩn rồi đem ngâm trong nước khoảng 5-6
                            tiếng cho đậu nở mềm.</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bạn
                            đem bột sắn dây hoà tan với nước sao cho không bị vón cục.</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bạn
                            cho nước vào dừa nạo, nhào và vắt lấy phần nước lọc.</span></li>
                </ul>
                <h4><span id="Buoc_2_Nau_che_hat_ke_dau_xanh"><b>Bước 2: Nấu chè hạt kê đậu
                            xanh</b></span></h4>
                <p><span style="font-weight: 400;">Bạn vớt hạt kê ra và cho vào nồi nước nấu cho đến
                        khi nở. Tiếp theo, bạn cho tiếp đậu xanh vào nồi nước hấp chín nhưng không
                        làm đậu nát. Tiếp đến, bạn trút phần đậu xanh vào nồi hạt kê và khuấy đều để
                        các nguyên liệu hoà tan vào nhau.</span></p>
                <p><span style="font-weight: 400;">Sau đó, bạn cho thêm đường vào nồi chè và nêm nếm
                        cho vừa ăn. Lúc này, bạn cho phần hạt sắn dây đã hoà tan vào và khuấy đều
                        cho đến khi chè sánh đặc lại thì có thể tắt bếp.</span></p>
                <h4><span id="Buoc_3_Nau_nuoc_cot_dua"><b>Bước 3: Nấu nước cốt dừa</b></span></h4>
                <p><span style="font-weight: 400;">Bạn cho vào nồi đường, muối, nước cốt dừa và 2
                        muỗng bột năng rồi khuấy đều. Sau đó, bạn cho nồi lên bếp và đun với lửa nhỏ
                        đến khi nước cốt dừa sền sệt là được.</span></p>
                <p><span style="font-weight: 400;">Vậy bạn là bạn đã biết <strong>cách nấu chè hạt
                            kê</strong> đậu xanh rồi đó. Bạn múc chè ra chén, rưới nước cốt dừa lên
                        trên là có thể thưởng thức rồi đấy. Để ngon hơn bạn có thể cho thêm bánh đa
                        nướng giòn rụm nhé!</span></p>
                <p><img decoding="async" class="size-full wp-image-12643 aligncenter"
                        src="https://amthucbonmua.vn/wp-content/uploads/2023/04/cach-nau-che-hat-ke-1.jpg"
                        alt="cách nấu chè hạt kê" width="600" height="400" /></p>
                <p style="text-align: center;"><em>Chè hạt kê đậu xanh</em></p>
                <h2><span id="2_Cach_nau_che_hat_ke_va_yen_mach_ngon_ngot_bui_beo"><b>2. Cách nấu
                            chè hạt kê và yến mạch ngon ngọt, bùi béo</b></span></h2>
                <h3><span id="Nguyen_lieu-2"><b>Nguyên liệu</b></span></h3>
                <ul>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">30g
                            hạt kê</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">30g
                            hạt sen</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">20g
                            yến mạch</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">30g
                            đậu xanh</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">1
                            chén bột sắn</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">20g
                            đường phèn</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Vani</span></li>
                </ul>
                <h3><span id="Cach_nau_che_hat_ke_hat_sen_va_yen_mach"><b>Cách nấu chè hạt kê, hạt
                            sen và yến mạch </b></span></h3>
                <h4><span id="Buoc_1_So_che_nguyen_lieu-2"><b>Bước 1: Sơ chế nguyên liệu</b></span>
                </h4>
                <ul>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bạn
                            cho hạt kê và đậu xanh vào ngâm trong nước khoảng 5-6 tiếng cho đến khi
                            nở thì vớt ra.</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Hạt
                            sen bạn ngâm trong trước khoảng 30 phút.</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bạn
                            cho yến mạch vào ngâm trong nước khoảng 60 phút.</span></li>
                    <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bạn
                            đem bột sắn dây hoà tan với nước.</span></li>
                </ul>
                <h4><span id="Buoc_2_Cach_nau_che_hat_ke_yen_mach"><b>Bước 2: Cách nấu chè hạt kê
                            yến mạch</b></span></h4>
                <p><span style="font-weight: 400;">Bạn cho hạt kê và đậu xanh vào nồi riêng nấu cho
                        đến khi chín. Hạt sen cũng vậy, bạn luộc hạt sen chín rồi để riêng. Sau đó,
                        bạn cho đậu xanh vào nồi hạt kê và khuấy đều tay. Tiếp tục bạn cho yến mạch
                        vào nấu khoảng 5 phút thì cho hạt sen vào khuấy đều. </span></p>
                <p><span style="font-weight: 400;">Tiếp theo, bạn cho đường phèn vào rồi tiếp tục
                        nấu cho đến khi hỗn hợp sôi lên thì cho bột sắn dây vào khuấy đều và tắt
                        bếp. Vậy là món chè hạt kê yến mạch đã hoàn thành, bạn có thể cho thêm vani
                        vào cho chè toả hương thơm là được.</span></p>
                <p><img decoding="async" loading="lazy" class="size-full wp-image-12644 aligncenter"
                        src="https://amthucbonmua.vn/wp-content/uploads/2023/04/cach-nau-che-hat-ke-2.jpg"
                        alt="cách nấu chè hạt kê" width="600" height="400" /></p>
                <p style="text-align: center;"><em>Chè hạt kê yến mạch</em></p>
